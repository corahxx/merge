def get_operator_name_from_table_name(table_name: str) -> str:
    """
    根据表名称（一般为文件名或上报机构名）匹配运营商名称。
    表名包含任一关键词则返回对应运营商名称；无匹配则返回空字符串。
    """
    if not table_name or not isinstance(table_name, str):
        return ""
    s = table_name.strip()
    for keywords, operator_name in OPERATOR_NAME_RULES:
        for kw in keywords:
            if kw in s:
                return operator_name
    return ""
